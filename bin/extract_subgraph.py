#!/usr/bin/env python3
"""Materialize the v2 subgraph: the human-only cut of the Monarch dump.

Reads v1's pinned KGX dump (KG-RAG-EDS/data/) and writes a derived subgraph into
v2's own data/ — nodes.tsv and edges.tsv — as this repo's source-of-truth corpus.
The dump itself is never edited; this is a filtered copy, regenerated by re-running,
not patched.

The cut is the method-neutral relevance boundary from doc/substack_draft.md section 1:
keep an edge only when neither endpoint belongs to a non-human species. A taxon-free
node (a MONDO disease, an HP phenotype) is species-neutral and stays; what disqualifies
an edge is an endpoint tagged as another organism. Nodes are then whatever those edges
touch. All columns are carried through so the text-building step has names, synonyms,
descriptions and readable predicates to work with.
"""

import duckdb

import shape_common as sc

# Expected size of the cut, from the measured relevance table. The run asserts
# against these so a silent change in the dump or the filter is caught, not shipped.
EXPECT_NODES = 442_307
EXPECT_EDGES = 4_923_997

OUT_NODES = sc.PROJECT_HOME / "data" / "nodes.tsv"
OUT_EDGES = sc.PROJECT_HOME / "data" / "edges.tsv"

con = duckdb.connect()

# in_taxon_label is the only column the filter reads; load the dumps as-is otherwise.
con.execute(f"""
    CREATE VIEW raw_nodes AS
    SELECT * FROM read_csv('{sc.NODES_TSV}', {sc.READ_OPTS})
""")
con.execute(f"""
    CREATE VIEW raw_edges AS
    SELECT * FROM read_csv('{sc.EDGES_TSV}', {sc.READ_OPTS})
""")

# Human (or taxon-free) means: no endpoint carries a non-human taxon label. Missing
# label is treated as human-compatible, matching shape_composition.py / shape_relevance.py.
con.execute("""
    CREATE TABLE kept_edges AS
    SELECT e.*
    FROM raw_edges e
    JOIN raw_nodes s ON s.id = e.subject
    JOIN raw_nodes o ON o.id = e.object
    WHERE coalesce(s.in_taxon_label, 'Homo sapiens') = 'Homo sapiens'
      AND coalesce(o.in_taxon_label, 'Homo sapiens') = 'Homo sapiens'
""")

con.execute("""
    CREATE TABLE kept_node_ids AS
    SELECT DISTINCT id FROM (
        SELECT subject AS id FROM kept_edges
        UNION ALL
        SELECT object FROM kept_edges
    )
""")
con.execute("""
    CREATE TABLE kept_nodes AS
    SELECT n.* FROM raw_nodes n
    JOIN kept_node_ids k ON k.id = n.id
""")

n_nodes = con.execute("SELECT count(*) FROM kept_nodes").fetchone()[0]
n_edges = con.execute("SELECT count(*) FROM kept_edges").fetchone()[0]

# A non-human node surviving would mean the filter leaked; check before writing.
leaked = con.execute("""
    SELECT count(*) FROM kept_nodes
    WHERE in_taxon_label IS NOT NULL AND in_taxon_label <> 'Homo sapiens'
""").fetchone()[0]

print(f"nodes: {n_nodes:,} (expected {EXPECT_NODES:,})")
print(f"edges: {n_edges:,} (expected {EXPECT_EDGES:,})")
print(f"non-human nodes surviving: {leaked:,}")

assert leaked == 0, f"{leaked:,} non-human nodes survived the cut"
assert n_nodes == EXPECT_NODES, f"node count {n_nodes:,} != expected {EXPECT_NODES:,}"
assert n_edges == EXPECT_EDGES, f"edge count {n_edges:,} != expected {EXPECT_EDGES:,}"

OUT_NODES.parent.mkdir(parents=True, exist_ok=True)
con.execute(f"COPY kept_nodes TO '{OUT_NODES}' (FORMAT csv, DELIMITER '\t', HEADER)")
con.execute(f"COPY kept_edges TO '{OUT_EDGES}' (FORMAT csv, DELIMITER '\t', HEADER)")
print(
    f"\nwrote {OUT_NODES.relative_to(sc.PROJECT_HOME)} and "
    f"{OUT_EDGES.relative_to(sc.PROJECT_HOME)}"
)
