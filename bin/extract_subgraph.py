#!/usr/bin/env python3
"""Materialize the v2 subgraph: the human-relevant cut of the Monarch dump.

Reads v1's pinned KGX dump (KG-RAG-EDS/data/) and writes a derived subgraph into
v2's own data/ — nodes.tsv and edges.tsv — as this repo's source-of-truth corpus.
The dump itself is never edited; this is a filtered copy, regenerated by re-running,
not patched.

The cut is the namespace allowlist from config/shape_probe.yaml, argued in
doc/substack_draft.md section 1: keep an edge only when BOTH endpoints sit in a
human-relevant or species-neutral namespace, with the non-human in_taxon_label guard
retained on top. Filtering by taxon label alone (an earlier version of this script)
leaked ~145k nodes of organism-specific phenotype and anatomy ontologies, because
those are ontology classes carrying no taxon label. All columns are carried through
so the text-building step has names, synonyms, descriptions and readable predicates.
"""

import duckdb

import shape_common as sc

# Expected size of the cut, measured from the dump under the allowlist. The run
# asserts these so a silent change in the dump or the filter is caught, not shipped.
EXPECT_NODES = 299_950
EXPECT_EDGES = 4_097_434

OUT_NODES = sc.PROJECT_HOME / "data" / "nodes.tsv"
OUT_EDGES = sc.PROJECT_HOME / "data" / "edges.tsv"

ALLOWLIST = sc.config()["namespace_allowlist"]

con = duckdb.connect()
con.execute("CREATE TABLE allow (prefix VARCHAR)")
con.executemany("INSERT INTO allow VALUES (?)", [[p] for p in ALLOWLIST])

# in_taxon_label is the only node column the filter reads; load the dumps as-is.
con.execute(f"""
    CREATE VIEW raw_nodes AS
    SELECT * FROM read_csv('{sc.NODES_TSV}', {sc.READ_OPTS})
""")
con.execute(f"""
    CREATE VIEW raw_edges AS
    SELECT * FROM read_csv('{sc.EDGES_TSV}', {sc.READ_OPTS})
""")

# Keep an edge when both endpoint namespaces are allowlisted AND neither endpoint
# carries a non-human taxon label. The namespace test is the primary cut; the taxon
# test is a guard against an allowlisted namespace with a stray non-human member.
con.execute("""
    CREATE TABLE kept_edges AS
    SELECT e.*
    FROM raw_edges e
    JOIN raw_nodes s ON s.id = e.subject
    JOIN raw_nodes o ON o.id = e.object
    WHERE split_part(e.subject, ':', 1) IN (SELECT prefix FROM allow)
      AND split_part(e.object, ':', 1) IN (SELECT prefix FROM allow)
      AND coalesce(s.in_taxon_label, 'Homo sapiens') = 'Homo sapiens'
      AND coalesce(o.in_taxon_label, 'Homo sapiens') = 'Homo sapiens'
""")

con.execute("""
    CREATE TABLE kept_node_ids AS
    SELECT DISTINCT id FROM (
        SELECT subject AS id FROM kept_edges
        UNION ALL
        SELECT object FROM kept_edges
    )
""")
con.execute("""
    CREATE TABLE kept_nodes AS
    SELECT n.* FROM raw_nodes n
    JOIN kept_node_ids k ON k.id = n.id
""")

n_nodes = con.execute("SELECT count(*) FROM kept_nodes").fetchone()[0]
n_edges = con.execute("SELECT count(*) FROM kept_edges").fetchone()[0]

# A node outside the allowlist, or one tagged non-human, would mean the filter
# leaked; both are checked before writing.
leaked_taxon = con.execute("""
    SELECT count(*) FROM kept_nodes
    WHERE in_taxon_label IS NOT NULL AND in_taxon_label <> 'Homo sapiens'
""").fetchone()[0]
leaked_ns = con.execute("""
    SELECT count(*) FROM kept_nodes
    WHERE split_part(id, ':', 1) NOT IN (SELECT prefix FROM allow)
""").fetchone()[0]

print(f"nodes: {n_nodes:,} (expected {EXPECT_NODES:,})")
print(f"edges: {n_edges:,} (expected {EXPECT_EDGES:,})")
print(f"non-human nodes surviving: {leaked_taxon:,}")
print(f"non-allowlisted nodes surviving: {leaked_ns:,}")

assert leaked_taxon == 0, f"{leaked_taxon:,} non-human nodes survived the cut"
assert leaked_ns == 0, f"{leaked_ns:,} non-allowlisted nodes survived the cut"
assert n_nodes == EXPECT_NODES, f"node count {n_nodes:,} != expected {EXPECT_NODES:,}"
assert n_edges == EXPECT_EDGES, f"edge count {n_edges:,} != expected {EXPECT_EDGES:,}"

OUT_NODES.parent.mkdir(parents=True, exist_ok=True)
con.execute(f"COPY kept_nodes TO '{OUT_NODES}' (FORMAT csv, DELIMITER '\t', HEADER)")
con.execute(f"COPY kept_edges TO '{OUT_EDGES}' (FORMAT csv, DELIMITER '\t', HEADER)")
print(
    f"\nwrote {OUT_NODES.relative_to(sc.PROJECT_HOME)} and "
    f"{OUT_EDGES.relative_to(sc.PROJECT_HOME)}"
)
